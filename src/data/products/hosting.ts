export function getHostingProducts(): Product[] {
  return hostingProducts.sort((a, b) => b.overall_rating - a.overall_rating);
}

export function getHostingProductBySlug(slug: string): Product | undefined {
  return hostingProducts.find((p) => p.slug === slug);
}

export function getFeaturedHostingProducts(): Product[] {
  return hostingProducts
    .filter((p) => p.is_featured)
    .sort((a, b) => b.overall_rating - a.overall_rating);
}

export function getEditorsChoiceHosting(): Product | undefined {
  return hostingProducts.find((p) => p.is_editors_choice);
}
