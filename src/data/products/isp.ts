export function getIspProducts(): ISPProduct[] {
  return ispProducts.sort((a, b) => b.overall_rating - a.overall_rating);
}

export function getIspProductBySlug(slug: string): ISPProduct | undefined {
  return ispProducts.find((p) => p.slug === slug);
}

export function getFeaturedIspProducts(): ISPProduct[] {
  return ispProducts
    .filter((p) => p.is_featured)
    .sort((a, b) => b.overall_rating - a.overall_rating);
}

export function getEditorsChoiceIsp(): ISPProduct | undefined {
  return ispProducts.find((p) => p.is_editors_choice);
}

// Get providers available in a specific state
export function getIspByState(stateCode: string): ISPProduct[] {
  return ispProducts
    .filter((p) => p.coverage_states.includes(stateCode.toUpperCase()))
    .sort((a, b) => b.overall_rating - a.overall_rating);
}

// Get providers by technology type
export function getIspByTechnology(
  technology: "fiber" | "cable" | "dsl" | "fixed_wireless" | "satellite"
): ISPProduct[] {
  return ispProducts
    .filter((p) => p.technologies.includes(technology))
    .sort((a, b) => b.overall_rating - a.overall_rating);
}
