export function getVpnProducts(): Product[] {
  return vpnProducts.sort((a, b) => b.overall_rating - a.overall_rating);
}

export function getVpnProductBySlug(slug: string): Product | undefined {
  return vpnProducts.find((p) => p.slug === slug);
}

export function getFeaturedVpnProducts(): Product[] {
  return vpnProducts
    .filter((p) => p.is_featured)
    .sort((a, b) => b.overall_rating - a.overall_rating);
}

export function getEditorsChoiceVpn(): Product | undefined {
  return vpnProducts.find((p) => p.is_editors_choice);
}
