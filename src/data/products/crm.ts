export function getCrmProducts(): Product[] {
  return crmProducts.sort((a, b) => b.overall_rating - a.overall_rating);
}

export function getCrmProductBySlug(slug: string): Product | undefined {
  return crmProducts.find((p) => p.slug === slug);
}

export function getFeaturedCrmProducts(): Product[] {
  return crmProducts
    .filter((p) => p.is_featured)
    .sort((a, b) => b.overall_rating - a.overall_rating);
}

export function getEditorsChoiceCrm(): Product | undefined {
  return crmProducts.find((p) => p.is_editors_choice);
}
