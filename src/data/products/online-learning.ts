export function getOnlineLearningProductBySlug(slug: string): Product | undefined {
  return onlineLearningProducts.find((p) => p.slug === slug);
}

export function getFeaturedOnlineLearningProducts(): Product[] {
  return onlineLearningProducts
    .filter((p) => p.is_featured)
    .sort((a, b) => b.overall_rating - a.overall_rating);
}

export function getEditorsChoiceOnlineLearning(): Product | undefined {
  return onlineLearningProducts.find((p) => p.is_editors_choice);
}
