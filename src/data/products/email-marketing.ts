export function getEmailMarketingProducts(): Product[] {
  return emailMarketingProducts.sort((a, b) => b.overall_rating - a.overall_rating);
}

export function getEmailMarketingProductBySlug(slug: string): Product | undefined {
  return emailMarketingProducts.find((p) => p.slug === slug);
}

export function getFeaturedEmailMarketingProducts(): Product[] {
  return emailMarketingProducts
    .filter((p) => p.is_featured)
    .sort((a, b) => b.overall_rating - a.overall_rating);
}

export function getEditorsChoiceEmailMarketing(): Product | undefined {
  return emailMarketingProducts.find((p) => p.is_editors_choice);
}
